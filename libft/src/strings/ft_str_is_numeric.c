#include "../../inc/libft.h"

int	ft_str_is_numeric(char *str)
{
	if ((*str == '-' || *str == '+'))
		str++;
	while (*str != '\0')
	{
		if (!(*str >= '0' && *str <= '9'))
			return (0);
		str++;
	}
	return (1);
}
